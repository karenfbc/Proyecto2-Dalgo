def floyd_warshall(weights):
    n = len(weights)
    dist = [row[:] for row in weights]  # Copia la matriz original

    # Actualizar distancias usando el algoritmo de Floyd-Warshall
    for k in range(n):
        for i in range(n):
            for j in range(n):
                if dist[i][j] > dist[i][k] + dist[k][j]:
                    dist[i][j] = dist[i][k] + dist[k][j]

    return dist

